#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '../..');
const schemaPath = resolve(repoRoot, 'db/schema.sql');
const apiContractsPath = resolve(repoRoot, 'packages/api-contracts/src/index.ts');
const outputPath = resolve(repoRoot, 'packages/api-contracts/src/generated.ts');
const checkOnly = process.argv.includes('--check');

const schemaSql = readFileSync(schemaPath, 'utf8');
const apiContracts = readFileSync(apiContractsPath, 'utf8');
const tableRows = [...schemaSql.matchAll(/create table if not exists ([\w_]+)\s*\(([\s\S]*?)\n\);/gi)].map((match) => {
  const name = match[1];
  const columns = match[2].split('\n')
    .map((line) => line.trim().replace(/,$/, ''))
    .filter((line) => line && !line.startsWith('--') && !/^(constraint|primary key|foreign key|unique|check)\b/i.test(line))
    .map((line) => {
      const column = line.match(/^("?[\w_]+"?)\s+(.+)$/);
      if (!column) return null;
      const definition = column[2].toLowerCase();
      const tsType = /\btext\[\]/.test(definition) ? 'string[]' : /\b(text|uuid|varchar|char|date|timestamptz|timestamp)\b/.test(definition) ? 'string' : /\b(jsonb|json)\b/.test(definition) ? 'Record<string, unknown>' : /\b(bigserial|bigint|serial|integer|numeric|decimal|double precision|real)\b/.test(definition) ? 'number' : /\bboolean\b/.test(definition) ? 'boolean' : 'unknown';
      return { name: column[1].replaceAll('"', ''), nullable: !/\bnot null\b/i.test(column[2]) && !/\bprimary key\b/i.test(column[2]), tsType };
    })
    .filter(Boolean);
  return { name, typeName: `${pascalCase(name)}Row`, columns };
});
const apiSchemas = [...apiContracts.matchAll(/export const (\w+)Schema\s*=/g)]
  .map((match) => match[1])
  .filter((name) => !['id', 'isoDateTime'].includes(name))
  .sort((left, right) => left.localeCompare(right));
const generated = [
  '// AUTO-GENERATED by scripts/codegen/generate-contract-types.mjs.',
  '// Do not edit by hand; run npm run codegen:types.',
  '',
  ...tableRows.flatMap((table) => [`export type ${table.typeName} = {`, ...table.columns.map((column) => `  ${JSON.stringify(column.name)}: ${column.tsType}${column.nullable ? ' | null' : ''};`), '};', '']),
  `export type GeneratedDbTableName = ${tableRows.map((table) => `'${table.name}'`).join(' | ')};`,
  '',
  'export type GeneratedDatabaseRows = {',
  ...tableRows.map((table) => `  ${JSON.stringify(table.name)}: ${table.typeName};`),
  '};',
  '',
  `export type GeneratedApiContractName = ${apiSchemas.map((name) => `'${name}'`).join(' | ')};`,
  '',
  'export type GeneratedApiContractDtoNames = {',
  ...apiSchemas.map((name) => `  ${name}: '${schemaTypeName(name)}Dto';`),
  '};',
  ''
].join('\n');

if (checkOnly) {
  if (readFileSync(outputPath, 'utf8') !== generated) {
    console.error(`${relative(repoRoot, outputPath)} is out of date. Run npm run codegen:types.`);
    process.exit(1);
  }
} else {
  writeFileSync(outputPath, generated);
}

function pascalCase(value) { return value.split('_').filter(Boolean).map((part) => part[0].toUpperCase() + part.slice(1)).join(''); }
function schemaTypeName(value) { return value.split(/(?=[A-Z])/).map((part) => part[0].toUpperCase() + part.slice(1)).join(''); }
